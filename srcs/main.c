#include "../includes/ft_printf.h"

int main()
{
    int test;
    test = 42;

    printf("\n-------------------------------------------------------------\n");
    ft_printf("char FT: %c %c\n", 'r', 'e');
    printf("char:   %c %c", 'r', 'e');
    printf("\n-------------------------------------------------------------\n");
    printf("\n-------------------------------------------------------------\n");
    ft_printf("int FT:  %d %i\n", 45, 54);
    printf("int:    %d %i", 45, 54);
    printf("\n-------------------------------------------------------------\n");
    printf("\n-------------------------------------------------------------\n");
    ft_printf("uint FT: %u\n", 1 << 31);
    printf("uint:   %u", 1 << 31);
    printf("\n-------------------------------------------------------------\n");
    printf("\n-------------------------------------------------------------\n");
    ft_printf("hex lower FT:    %x\n", 0xabcf012);
    printf("hex lower:  %x", 0xabcf012);
    printf("\n-------------------------------------------------------------\n");
    printf("\n-------------------------------------------------------------\n");
    ft_printf("hex upper FT:    %X\n", 0xabcf012);
    printf("hex upper:  %X", 0xabcf012);
    printf("\n-------------------------------------------------------------\n");
    printf("\n-------------------------------------------------------------\n");
    ft_printf("percent FT:  %%\n");
    printf("percent:    %%");
    printf("\n-------------------------------------------------------------\n");
    printf("\n-------------------------------------------------------------\n");
    ft_printf("pointer FT:  %p\n", &test);
    printf("pointer:    %p", &test);
    printf("\n-------------------------------------------------------------\n");
    printf("\n-------------------------------------------------------------\n");
    ft_printf("string FT:   %s\n", "klk");
    printf("string: %s", "klk");
    printf("\n-------------------------------------------------------------\n");
}